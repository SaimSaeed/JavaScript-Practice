var a = "E"

switch(a){
case "A":
    console.log("This is a Vowel");
    break;
    case "E":
    console.log("This is a Vowel");  // here if we want to cases to run together we remove break 
    case "I":
    console.log("This is a Vowel");
    break;
    case "O":
    console.log("This is a Vowel");
    break;
    case "U":
    console.log("This is a Vowel");
    break;

    default:
        console.log("Please enter valid value")


}



